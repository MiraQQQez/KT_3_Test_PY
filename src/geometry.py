class Geometry:
    """
    Класс для геометрических операций.
    Предоставляет методы для работы с геометрическими фигурами.
    """

    @staticmethod
    def classify_triangle(a: float, b: float, c: float) -> str:
        """
        Классифицирует треугольник по длинам его сторон.
        
        Args:
            a (float): Длина первой стороны
            b (float): Длина второй стороны
            c (float): Длина третьей стороны
            
        Returns:
            str: Тип треугольника:
                - "равносторонний" - все стороны равны
                - "равнобедренный" - две стороны равны
                - "разносторонний" - все стороны разные
                - "не треугольник" - стороны не образуют треугольник
                
        Raises:
            ValueError: Если любая из сторон отрицательная или равна нулю
            
        Examples:
            >>> Geometry.classify_triangle(5, 5, 5)
            'равносторонний'
            >>> Geometry.classify_triangle(5, 5, 3)
            'равнобедренный'
            >>> Geometry.classify_triangle(3, 4, 5)
            'разносторонний'
        """
        # Проверка на положительные значения
        if a <= 0 or b <= 0 or c <= 0:
            raise ValueError("Все стороны треугольника должны быть положительными числами")
        
        # Проверка неравенства треугольника
        # Сумма любых двух сторон должна быть больше третьей стороны
        if a + b <= c or a + c <= b or b + c <= a:
            return "не треугольник"
        
        # Проверка типа треугольника
        if a == b == c:
            return "равносторонний"
        elif a == b or b == c or a == c:
            return "равнобедренный"
        else:
            return "разносторонний"